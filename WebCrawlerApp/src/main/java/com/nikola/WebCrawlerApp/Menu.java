    public static void selectSite() throws IOException {
        Scanner scanner = new Scanner(System.in);
        System.out.println();
        System.out.println("   Welcome to MetCrawler.");
        System.out.println("   First time here? Type help\n");
        while(true){
            char letter = 0;
            char space = 32;
            System.out.print(">> ");
            String input = scanner.nextLine();
            if(input.contains("crawl 1 from")){
                String temp[] = input.split("from");
                String stringLetter = temp[1].trim();
                letter = stringLetter.charAt(0);
            }
            input.trim();
            input.toUpperCase();

            if(input.equals("crawl 1")){
                System.out.println("   Crawling Textovi.net");
                TextoviNet.crawl();
                break;
            }else if(input.equals("crawl 2")){
                //neki drugi sajt
                System.out.println("   Crawling HellasSongs");
                HellasSongs.crawl();
                break;
            }else if(letter > 63 && letter <91 && input.equals("crawl 1 from "+letter)){
                System.out.println("   Crawling Textovi.net starting from letter "+letter);
                TextoviNet.fromSpecificLetter(letter);
                TextoviNet.crawl();

            }
            else if(input.equals("help")){
                System.out.println("   crawl x - crawls site that numbered as x ");
                System.out.println("   list - lists all the sites that are available for crawling");
                System.out.println("   info - prints info about MetCrawler");
                System.out.println("   crawl 1 from x - crawls from specific letter (NOTE: Currently supported only for Textovi.net!)");

            }else if(input.equals("list")){
                System.out.println("   1. Textovi.net");
                System.out.println("   2. HellasSongs");
            }else if(input.equals("info")){
                char a = 92;
                System.out.println(" "+a+""+a+" This application is made for the final project of CS101 course. //");
                System.out.println("                   "+a+a+" Developed by: Nikola Mitrovic. //");
            }else if(input.equals("quit")||input.equals("exit")){
                System.out.println("   Goodbye!");
                System.exit(0);
            }
            else{
                System.out.println("   Please enter some valid input.");
            }
        }
    }



